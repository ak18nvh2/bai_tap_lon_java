    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        
        String maGT = tf_maGiaoTrinh.getText();
        String tenGT = tf_tenGiaoTrinh.getText();
        String hoTenTG = tf_hoTenTG.getText();
        String soLuong = tf_soLuong.getText();
        String soTrang = tf_soTrang.getText();
        String ngayXB = tf_ngay.getText();
        String loai = "";
        if (rb_CB.isSelected()) {
            loai = "CB";
        } else if (rb_NC.isSelected()) {
            loai = "NC";
        }
        if (maGT.equals("") || tenGT.equals("") || hoTenTG.equals("") || soLuong.equals("") || soTrang.equals("") || ngayXB.equals("") || loai.equals("")) {
            JOptionPane.showMessageDialog(null, "Cần nhập đủ tất cả các thông tin!", "Thông báo", JOptionPane.ERROR_MESSAGE);
        } else {
            ResultSet rs;
            try {
                rs = gt.ShowGiaoTrinhTheoMa(maGT);
                if (rs.next()) {
                    JOptionPane.showMessageDialog(null, "Mã giáo trình đã tồn tại!", "Thông báo", JOptionPane.ERROR_MESSAGE);
                } else {
                    try {
                        gt.InSertGiaoTrinh(maGT, tenGT, hoTenTG, soLuong, soTrang, ngayXB, loai);
                        
                        JOptionPane.showMessageDialog(null, "Thêm thành công!", "Thông báo", JOptionPane.PLAIN_MESSAGE);
                        showData();
                    } catch (SQLException ex) {
                       // ex.printStackTrace();
                        JOptionPane.showMessageDialog(null, "Cần nhập ngày xuất bản theo định dạng Năm-Tháng-Ngày!", "Thông báo", JOptionPane.ERROR_MESSAGE);
                    } catch (ClassNotFoundException ex) {
                      //  ex.printStackTrace();
                        //JOptionPane.showMessageDialog(null, "Thêm thất bại2!", "Thông báo", JOptionPane.ERROR_MESSAGE);
                    }
                }
            } catch (ClassNotFoundException ex) {
                Logger.getLogger(GiaoTrinh2.class.getName()).log(Level.SEVERE, null, ex);
            } catch (SQLException ex) {
                Logger.getLogger(GiaoTrinh2.class.getName()).log(Level.SEVERE, null, ex);
            }

        }

    }//GEN-LAST:event_jButton1ActionPerformed

