    public void showData() throws ClassNotFoundException, SQLException{
        DefaultTableModel tbModel= new DefaultTableModel();
        String colsName[]={"Mã giáo trình","Tên giáo trình","Họ tên tác giả","Số lượng giáo trình","Đơn giá","Ngày xuất bản"};
        tbModel.setColumnIdentifiers(colsName);
        ResultSet rs= gt.ShowGiaoTrinh();
        while(rs.next()){
            String []row= new String[6];
            row[0]=rs.getString("MaGT");
            row[1]=rs.getString("TenGT");
            row[2]=rs.getString("HoTenTG");
            row[3]=rs.getString("Soluong");
            double donGia=0;
            int soTrang=Integer.parseInt(rs.getString("Sotrang"));
            String loai=rs.getString("Loai");
            if(loai.equals("CB"))
                donGia=soTrang*250;
            else donGia=soTrang*400;
            row[4]=donGia+"";
            row[5]=rs.getString("NgayXB");
            tbModel.addRow(row);
            
        }
        tb_data.setModel(tbModel);
    }
