    private void tb_dataMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tb_dataMouseClicked
        // TODO add your handling code here:
        int row= tb_data.getSelectedRow();
        String maGT= (String) tb_data.getModel().getValueAt(row, 0);
        ResultSet rs;
        try {
            rs = gt.ShowGiaoTrinhTheoMa(maGT);
            if(rs.next()){
            tf_hoTenTG.setText(rs.getString("HoTenTG"));
            tf_maGiaoTrinh.setText(rs.getString("MaGT"));
            tf_ngay.setText(rs.getString("NgayXB"));
            tf_soLuong.setText(rs.getString("Soluong"));
            tf_soTrang.setText(rs.getString("Sotrang"));
            if(rs.getString("Loai").equals("CB")) rb_CB.setSelected(true);
            else rb_NC.setSelected(true);
            tf_tenGiaoTrinh.setText(rs.getString("TenGT"));
        }
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(GiaoTrinh2.class.getName()).log(Level.SEVERE, null, ex);
        } catch (SQLException ex) {
            Logger.getLogger(GiaoTrinh2.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }//GEN-LAST:event_tb_dataMouseClicked

