    private void jTable2MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable2MouseClicked
        // TODO add your handling code here:
        try{
            int row= this.jTable2.getSelectedRow();
            String maLoai=  (String) jTable2.getModel().getValueAt(row, 0);
            ResultSet rs= loaisp.ShowLoaiSP(maLoai);
            if(rs.next()){
                this.tf_maLoai.setText(rs.getString("Maloai"));
                this.tf_tenLoai.setText(rs.getString("Tenloai"));
            }
        }
        catch(SQLException e){
            e.printStackTrace();
        }
    }//GEN-LAST:event_jTable2MouseClicked

    private void xoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_xoaActionPerformed
        // TODO add your handling code here:
        String maLoai= tf_maLoai.getText();
        try{
            if(maLoai.equals(""))
                JOptionPane.showMessageDialog(null, "Chọn 1 loại sản phẩm để xóa!", "Thông báo",JOptionPane.ERROR_MESSAGE);
            else{
                loaisp.DeleteData(maLoai);
                ClearData();
                ShowData();
            }
        }catch(SQLException e){
            JOptionPane.showMessageDialog(this, "Khong xoa đc");
        }
    }//GEN-LAST:event_xoaActionPerformed

