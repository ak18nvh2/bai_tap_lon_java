    private void suaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_suaActionPerformed
        // TODO add your handling code here:
        String maLoai=tf_maLoai.getText();
        if(maLoai.equals(""))
            JOptionPane.showMessageDialog(this, "chưa chọn", "thông báo"   , JOptionPane.ERROR_MESSAGE);
        else{
            setKhoa(false);
            tf_maLoai.enable(false);
            setButton(false);
            cothem=false;
        }
    }//GEN-LAST:event_suaActionPerformed

    private void luuActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_luuActionPerformed
        // TODO add your handling code here:
        String maLoai=tf_maLoai.getText();
        String tenLoai=tf_tenLoai.getText();
        if(maLoai.equals("") || tenLoai.equals(""))
            JOptionPane.showMessageDialog(this, "chưa nhập đủ", "thông báo", JOptionPane.ERROR_MESSAGE);
        else if(maLoai.length()>2 || tenLoai.length()>20){
            JOptionPane.showMessageDialog(this, "Mã loại tối đa chỉ được 2 ký tự và tên loại tối đa chỉ được 20 ký tự!","Thông báo",JOptionPane.ERROR_MESSAGE);
        }
        else{
            try{
                if(cothem==true){
                    loaisp.InsertData(maLoai, tenLoai);
                }
                else{
                    loaisp.EditData(maLoai, tenLoai);
                }
                ClearData();
                ShowData();
            }
            catch(SQLException e){
                JOptionPane.showMessageDialog(this, "Cập nhật thất bại!", "Thông báo", JOptionPane.ERROR_MESSAGE);
            }
            setKhoa(true);
            setButton(true);
        }
    }//GEN-LAST:event_luuActionPerformed

